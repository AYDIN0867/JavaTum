package ders16_For_Loop;

public class C02_Dikkat_Edilecekler {

    public static void main(String[] args) {

        for (int i = 10; i >0 ; i++) {

            System.out.println(i+" ");

        }
        // sıfırdan küçük olma drumu olamdığı için sonsuz loop oluşur
        // conditionun hep true olup olmadığına bakmak lazım
    }
}
