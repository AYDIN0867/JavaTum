package TEKRAR.Konu06_IncrementDecrement;

public class C01_Increment_Decrement {
    public static void main(String[] args) {
        int sayi=20; //sayının degerini 2 katına çıkarın

        sayi=sayi*2;
        sayi*=2;
        System.out.println("9.satırda sayı : "+sayi);//80

        sayi-=5;
        System.out.println("12.saturda sayının degeri : "+sayi);//75

//sayiyi 5 e bölüp,sonucu sayi variable^na atayin

        sayi=sayi/5;
        System.out.println("17. satırda sayının degeri : "+sayi);//15




    }
}
